    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
        try {
        Room room = new Room();

        room.setRoomNumber(jTextFieldRoomNumber.getText().trim());
        room.setRoomType(jComboBoxRoomType.getSelectedItem().toString());
        room.setPricePerNight(Double.parseDouble(jTextFieldPricePerNight.getText()));
        room.setStatus(jComboBoxStatus.getSelectedItem().toString());
        room.setCapacity(Integer.parseInt(jTextFieldCapacity.getText()));
        room.setDescription(jTextAreaDescription.getText());

        int result;

        if (selectedId == null) {
            result = controller.create(room);
        } else {
            result = controller.update(room, selectedId);
        }

        if (result == 1) {

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
            clearForm();
            loadRoomData();

        } else {

            JOptionPane.showMessageDialog(this, "Data gagal disimpan.");

        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(this, e.getMessage());

    }
    }//GEN-LAST:event_jButtonSaveActionPerformed

