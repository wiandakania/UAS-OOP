    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedId == null) {
        JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(
            this,
            "Yakin ingin menghapus data?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {

        int result = controller.delete(selectedId);

        if (result == 1) {
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
            clearForm();
            loadRoomData();
        } else {
            JOptionPane.showMessageDialog(this, "Data gagal dihapus.");
        }

     }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
            try {

        int id = Integer.parseInt(jTextFieldSearchId.getText());

        Room room = controller.search(id);

        if (room != null) {

            selectedId = room.getId();

            jTextFieldRoomNumber.setText(room.getRoomNumber());
            jComboBoxRoomType.setSelectedItem(room.getRoomType());
            jTextFieldPricePerNight.setText(String.valueOf(room.getPricePerNight()));
            jComboBoxStatus.setSelectedItem(room.getStatus());
            jTextFieldCapacity.setText(String.valueOf(room.getCapacity()));
            jTextAreaDescription.setText(room.getDescription());

        } else {

            JOptionPane.showMessageDialog(this, "Data tidak ditemukan.");

        }

    } catch (NumberFormatException e) {

        JOptionPane.showMessageDialog(this, "ID harus berupa angka.");

    }
    }//GEN-LAST:event_jButtonSearchActionPerformed

