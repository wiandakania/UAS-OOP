    private void loadRoomData() {

    int totalData = controller.count();
    totalPage = (int) Math.ceil((double) totalData / limit);

    if (totalPage == 0) {
        totalPage = 1;
    }

    if (currentPage > totalPage) {
        currentPage = totalPage;
    }

    List<Room> rooms = controller.getPage(currentPage);

    String data[][] = new String[rooms.size()][7];

    for (int i = 0; i < rooms.size(); i++) {

        data[i][0] = String.valueOf(rooms.get(i).getId());
        data[i][1] = rooms.get(i).getRoomNumber();
        data[i][2] = rooms.get(i).getRoomType();
        data[i][3] = String.valueOf(rooms.get(i).getPricePerNight());
        data[i][4] = rooms.get(i).getStatus();
        data[i][5] = String.valueOf(rooms.get(i).getCapacity());
        data[i][6] = rooms.get(i).getDescription();
    }

    String column[] = {
        "ID",
        "No. Kamar",
        "Tipe",
        "Harga",
        "Status",
        "Kapasitas",
        "Deskripsi"
    };

    jTableRoom.setModel(new javax.swing.table.DefaultTableModel(data, column));

    jButtonPrev.setEnabled(currentPage > 1);
    jButtonNext.setEnabled(currentPage < totalPage);
}
 
