    private void loadTable() {

    tableModel.setRowCount(0);

    List<Employee> list = controller.getPage(currentPage);

    for (Employee e : list) {

        tableModel.addRow(new Object[]{
            e.getId(),
            e.getName(),
            e.getEmail(),
            e.getPhone(),
            e.getPosition(),
            e.getSalary(),
            e.getHireDate(),
            e.getStatus()
        });

    }

}
    
