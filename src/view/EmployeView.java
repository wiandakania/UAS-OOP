    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:
    try {
        // Pastikan user sudah memilih data dari tabel
        if (selectedId == 0) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diubah!");
            return;
        }
        
        if (jFormattedTextFieldHireD.getText().trim().isEmpty()) {
    JOptionPane.showMessageDialog(this,
            "Hire Date harus diisi (yyyy-MM-dd)");
    return;
}
        
        Employee e = new Employee();

        if (jTextFieldSalary.getText().trim().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Salary harus diisi");
    return;
}
        e.setId(selectedId);
        e.setName(jTextFieldName.getText());
        e.setEmail(jTextFieldEmail.getText());
        e.setPhone(jTextFieldPhone.getText());
        e.setPosition(jTextFieldPosition.getText());
        e.setSalary(Double.parseDouble(jTextFieldSalary.getText()));
        e.setHireDate(LocalDate.parse(jFormattedTextFieldHireD.getText()));
        e.setStatus(jTextFieldStatus.getText());

        int hasil = controller.update(e, selectedId);

        if (hasil > 0) {

            currentPage = 1;

            JOptionPane.showMessageDialog(this, "Data berhasil diubah");

            loadTable();

            clearForm();

        } else if (hasil == -1) {

            JOptionPane.showMessageDialog(this, "Data belum lengkap!");

        } else {

            JOptionPane.showMessageDialog(this, "Data gagal diubah!");

        }

    } catch (NumberFormatException ex) {

        JOptionPane.showMessageDialog(this, "Salary harus berupa angka!");

    } catch (Exception ex) {

        JOptionPane.showMessageDialog(this, ex.getMessage());

    }
    }//GEN-LAST:event_jButtonEditActionPerformed

