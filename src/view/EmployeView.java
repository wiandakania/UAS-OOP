    private void jButtonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCariActionPerformed
        // TODO add your handling code here:
        try{
    if (jTextFieldCari.getText().trim().isEmpty()) {

    JOptionPane.showMessageDialog(this, "Masukkan ID!");

    return;

}
    int id = Integer.parseInt(jTextFieldCari.getText());

    Employee e = controller.search(id);

    if(e!=null){

        selectedId = e.getId();
        currentPage = 1;

        jTextFieldName.setText(e.getName());
        jTextFieldEmail.setText(e.getEmail());
        jTextFieldPhone.setText(e.getPhone());
        jTextFieldPosition.setText(e.getPosition());
        jTextFieldSalary.setText(String.valueOf(e.getSalary()));
        jFormattedTextFieldHireD.setText(e.getHireDate().toString());
        jTextFieldStatus.setText(e.getStatus());

    }else{

        JOptionPane.showMessageDialog(this,"Data tidak ditemukan");

    }

}catch(Exception ex){

    JOptionPane.showMessageDialog(this,ex.getMessage());

}
    }//GEN-LAST:event_jButtonCariActionPerformed

