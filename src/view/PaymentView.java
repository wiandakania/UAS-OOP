    private void loadTable() {
    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("Payment ID");
    model.addColumn("Reservation ID");
    model.addColumn("Guest");
    model.addColumn("Room");
    model.addColumn("Amount");
    model.addColumn("Method");
    model.addColumn("Status");

    List<Payment> list = paymentController.getPage(currentPage);

    for (Payment p : list) {
        model.addRow(new Object[]{
            p.getId(),
            p.getReservationId(),
            p.getGuestName(),
            p.getRoomNumber(),
            p.getAmount(),
            p.getMethod(),
            p.getStatus()
        });
    }
    jTable1.setModel(model);

}

    private void loadReservationList() {
    List<Reservation> list = reservationController.getAll();

    jComboBoxId.removeAllItems();

    for (Reservation r : list) {
        jComboBoxId.addItem(String.valueOf(r.getId()));
    }
    }
    private void loadReservationData(int id) {

    Reservation r = reservationController.search(id);

    if (r == null) {
        JOptionPane.showMessageDialog(this, "Reservation tidak ditemukan");
        return;
    }

    jComboBoxNama.removeAllItems();
    jComboBoxNama.addItem(r.getGuestName());

    jComboBoxKamar.removeAllItems();
    jComboBoxKamar.addItem(r.getRoomNumber());

    jTextFieldCheckIn.setText(r.getCheckIn().toString());
    jTextFieldcCheckOut.setText(r.getCheckOut().toString());

    long nights = java.time.temporal.ChronoUnit.DAYS.between(
            r.getCheckIn(), r.getCheckOut());

    jTextFieldTotalNights.setText(String.valueOf(nights));
    jTextFieldTotalPayment.setText(String.valueOf(r.getTotalPrice()));
}

