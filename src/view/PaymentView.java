    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:                                      
    try {
        if (jComboBoxId.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Pilih Reservation ID dulu!");
            return;
        }

        int reservationId = Integer.parseInt(jComboBoxId.getSelectedItem().toString());

        String method = jComboBoxPaymentMethod.getSelectedItem().toString();
        double paid = Double.parseDouble(jTextFieldPaid.getText());
        double total = Double.parseDouble(jTextFieldTotalPayment.getText());

        double change = paid - total;
        jTextFieldChange.setText(String.valueOf(change));

        Payment p = new Payment();
        p.setReservationId(reservationId);
        p.setMethod(method);
        p.setAmount(paid);
        p.setStatus("PAID");

        // kalau field change ada di model
        try {
        } catch (Exception ignore) {}

        int result = paymentController.create(p);

        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Payment berhasil disimpan!");
            loadTable();
        } else {
            JOptionPane.showMessageDialog(this, "Payment gagal!");
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Input angka tidak valid!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

