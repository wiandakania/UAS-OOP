    private void jButtonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCariActionPerformed
        // TODO add your handling code here:

        try{

            int id = Integer.parseInt(jTextFieldPencarian.getText());

            Guest guest = guestController.search(id);

            if(guest!=null){

                selectedId = guest.getId();

                jTextFieldNama.setText(guest.getName());
                jTextFieldEmail.setText(guest.getEmail());
                jTextFieldPhone.setText(guest.getPhone());
                jTextFieldIdNumber.setText(guest.getIdNumber());
                jTextFieldAlamat.setText(guest.getAddress());

            }else{

                JOptionPane.showMessageDialog(this,"Data tidak ditemukan");

            }

        }catch(Exception e){

            JOptionPane.showMessageDialog(this,"ID harus berupa angka");

        }
    }//GEN-LAST:event_jButtonCariActionPerformed

    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        // TODO add your handling code here:

        Guest guest = new Guest();

        guest.setName(jTextFieldNama.getText());
        guest.setEmail(jTextFieldEmail.getText());
        guest.setPhone(jTextFieldPhone.getText());
        guest.setIdNumber(jTextFieldIdNumber.getText());
        guest.setAddress(jTextFieldAlamat.getText());

        int hasil = guestController.create(guest);

        if (hasil > 0) {

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan");

            loadTable();
            clearForm();

        } else {

            JOptionPane.showMessageDialog(this, "Data gagal disimpan");

        }
    }//GEN-LAST:event_jButtonCreateActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:

        if(selectedId==0){

            JOptionPane.showMessageDialog(this,"Pilih data terlebih dahulu");
            return;

        }

        Guest guest = new Guest();
        guest.setId(selectedId);

        guest.setName(jTextFieldNama.getText());
        guest.setEmail(jTextFieldEmail.getText());
        guest.setPhone(jTextFieldPhone.getText());
        guest.setIdNumber(jTextFieldIdNumber.getText());
        guest.setAddress(jTextFieldAlamat.getText());

        int hasil = guestController.update(guest, selectedId);

        if(hasil>0){

            JOptionPane.showMessageDialog(this,"Data berhasil diupdate");

            loadTable();
            clearForm();

        }else{

            JOptionPane.showMessageDialog(this,"Update gagal");

        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

