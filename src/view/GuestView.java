    private void loadTable() {
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    model.setRowCount(0);

    int offset = (currentPage - 1) * limit;

    List<Guest> list = guestController.getPage(currentPage);

    for (Guest g : list) {

        Object[] row = {
            g.getId(),
            g.getName(),
            g.getEmail(),
            g.getPhone(),
            g.getIdNumber(),
            g.getAddress()
        };

        model.addRow(row);
    }
}
    private void clearForm() {

    selectedId = 0;

    jTextFieldNama.setText("");
    jTextFieldEmail.setText("");
    jTextFieldPhone.setText("");
    jTextFieldIdNumber.setText("");
    jTextFieldAlamat.setText("");
    jTextFieldPencarian.setText("");

    jTable2.clearSelection();
}
    private void nextPage() {

    int totalPage = (int) Math.ceil((double) guestController.count() / limit);

    if (currentPage >= totalPage) {
        JOptionPane.showMessageDialog(this, "Ini halaman terakhir");
        return;
    }

    currentPage++;
    loadTable();
}
    private void prevPage() {

    if (currentPage <= 1) {
        JOptionPane.showMessageDialog(this, "Ini halaman pertama");
        return;
    }

    currentPage--;
    loadTable();
}
