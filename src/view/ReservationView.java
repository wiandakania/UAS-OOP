    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        // TODO add your handling code here:
        try {
        int guestId = getSelectedGuestId();
        int roomId = getSelectedRoomId();

        LocalDate checkIn = parseDate(jTextFieldCheckIn.getText());
        LocalDate checkOut = parseDate(jTextFieldCheckOut.getText());

        Reservation r = new Reservation();
        r.setGuestId(guestId);
        r.setRoomId(roomId);
        r.setCheckIn(checkIn);
        r.setCheckOut(checkOut);
        r.setStatus("BOOKED");

        reservationController.create(r); // <-- pastiin ada di controller

        JOptionPane.showMessageDialog(this, "Reservation berhasil dibuat!");
        refreshAll();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButtonCreateActionPerformed

