    private void jButtonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCariActionPerformed
        // TODO add your handling code here:
            int keyword;

    try {
        keyword = Integer.parseInt(jTextFieldPencarian.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan ID angka saja!");
        return;
    }

Reservation r = reservationController.search(keyword);

    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Guest");
    model.addColumn("Room");
    model.addColumn("Check In");
    model.addColumn("Check Out");
    model.addColumn("Total");
    model.addColumn("Status");


if (r != null) {
    model.addRow(new Object[]{
        r.getId(),
        r.getGuestName(),
        r.getRoomNumber(),
        r.getCheckIn(),
        r.getCheckOut(),
        r.getTotalPrice(),
        r.getStatus()
    });
}

jTableReservation.setModel(model);

    jTableReservation.setModel(model);
    }//GEN-LAST:event_jButtonCariActionPerformed

