    private void loadGuest() {
        jComboBoxGuest.removeAllItems();
        List<Guest> guests = guestController.getAll();

        for (Guest g : guests) {
            jComboBoxGuest.addItem(g.getId() + " - " + g.getName());
        }
    }

    private void loadRoom() {
        jComboBoxRoom.removeAllItems();
        List<Room> rooms = roomController.getAvailable();

        for (Room r : rooms) {
            jComboBoxRoom.addItem(r.getId() + " - " + r.getRoomNumber());
        }
    }

    private void loadTable() {
        DefaultTableModel model = new DefaultTableModel();

        model.addColumn("ID");
        model.addColumn("Guest");
        model.addColumn("Room");
        model.addColumn("Check In");
        model.addColumn("Check Out");
        model.addColumn("Total");
        model.addColumn("Status");

        List<Reservation> list = reservationController.getPage(currentPage);

        for (Reservation r : list) {
            model.addRow(new Object[]{
                r.getId(),
                r.getGuestName(),
                r.getRoomNumber(),
                r.getCheckIn(),
                r.getCheckOut(),
                r.getTotalPrice(),
                r.getStatus()
            });
        }

        jTableReservation.setModel(model);
    }

    private void refreshAll() {
        loadGuest();
        loadRoom();
        loadTable();
    }


    private int getSelectedGuestId() {
        Object selected = jComboBoxGuest.getSelectedItem();
        if (selected == null) return -1;
        return Integer.parseInt(selected.toString().split(" - ")[0]);
    }

    private int getSelectedRoomId() {
        Object selected = jComboBoxRoom.getSelectedItem();
        if (selected == null) return -1;
        return Integer.parseInt(selected.toString().split(" - ")[0]);
    }

    private LocalDate parseDate(String text) {
        return LocalDate.parse(text, formatter);
    }
    
